package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        int i = 128;
        System.out.println("int i is: " + i);

        short j = 12;
        System.out.println("short j is: " + j);


        long k = 158L;
        System.out.println("long k is: " + k);

        byte l = -128;
        System.out.println("byte l is: " + l);

        float m = 12345.6789F;
        System.out.println("float m is: " + m);

        double n = -12.34567890;
        System.out.println("double n is: " + n);

        char o = 'a';
        System.out.println("char o is: " + o);

        boolean p = false;
        System.out.println("boolean p is: " + p);

    }

}
